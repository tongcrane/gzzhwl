package com.gzzhwl.api.exception;

public class CaptchaServiceException extends RuntimeException {
	private static final long serialVersionUID = 7786257477612659782L;

	public CaptchaServiceException() {
		super();
	}

	public CaptchaServiceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CaptchaServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public CaptchaServiceException(String message) {
		super(message);
	}

	public CaptchaServiceException(Throwable cause) {
		super(cause);
	}
}

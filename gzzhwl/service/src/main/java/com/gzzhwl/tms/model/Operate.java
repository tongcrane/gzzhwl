package com.gzzhwl.tms.model;

import lombok.Getter;

@Getter
public enum Operate {
	CONFIRM("01", "确认发布"),

	REJECT("02", "拒绝发布"),

	CLOSE("03", "关闭货源"),

	INVALID("04", "毁约货源");

	private String code;
	private String name;

	private Operate(String code, String name) {
		this.code = code;
		this.name = name;
	}
}

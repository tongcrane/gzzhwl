package com.gzzhwl.admin.consignment.vo;

public class ConsignmentInfoVo {
	private String orderId; // 订单标识
	private String freightPrice; // 运费
	private String loadPrice; // 装卸费
	private String otherPrice; // 其他费用
	private String otherExplain; // 其他费用说明
	private String total; // 总费用
	private String goodsWeight; // 货物重量
	private String goodsVolume; // 货物体积
	private String remark; // 备注
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getFreightPrice() {
		return freightPrice;
	}
	public void setFreightPrice(String freightPrice) {
		this.freightPrice = freightPrice;
	}
	public String getLoadPrice() {
		return loadPrice;
	}
	public void setLoadPrice(String loadPrice) {
		this.loadPrice = loadPrice;
	}
	public String getOtherPrice() {
		return otherPrice;
	}
	public void setOtherPrice(String otherPrice) {
		this.otherPrice = otherPrice;
	}
	public String getOtherExplain() {
		return otherExplain;
	}
	public void setOtherExplain(String otherExplain) {
		this.otherExplain = otherExplain;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getGoodsWeight() {
		return goodsWeight;
	}
	public void setGoodsWeight(String goodsWeight) {
		this.goodsWeight = goodsWeight;
	}
	public String getGoodsVolume() {
		return goodsVolume;
	}
	public void setGoodsVolume(String goodsVolume) {
		this.goodsVolume = goodsVolume;
	}


}